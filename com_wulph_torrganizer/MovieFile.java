import java.io.File;


public class MovieFile extends TFile
{
    //===============================================================
    //      Constants
    //===============================================================


    //===============================================================
    //      Instance Variables
    //===============================================================
    private int releaseYear;

    //===============================================================
    //      Constructors
    //===============================================================

    public MovieFile(File file, String name, String extension, int year)
    {
        super(file, name, extension);

        releaseYear = year;
    }

    //===============================================================
    //      getters / setters
    //===============================================================

    //=================================
    //      yearYear
    //=================================

    public int getReleaseYear()
    {
        return releaseYear;
    }

    public void setReleaseYear(int year)
    {
        releaseYear = year;
    }
    
    //===============================================================
    //      conditionals
    //===============================================================

    public boolean isMovie() { return true; }

    //===============================================================
    //      toString
    //===============================================================

    public String toString()
    {
        String returnVal = super.toString() + 
                           "releaseYear:\t" + releaseYear + "\n";
        return returnVal;
    }

}
